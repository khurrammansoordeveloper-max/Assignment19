// 1. Declare variables
// 'const' is used for values that won't be reassigned
const name = "Alex"; 
const city = "New York";

// 'let' is used because age will change over time
let age = 25;

// 2. Print initial values
console.log("--- Initial Profile ---");
console.log("Name:", name);
console.log("Age:", age);
console.log("City:", city);

// 3. Update the age variable
age = 26; // Happy Birthday!

// 4. Print again to see the update
console.log("--- Updated Profile ---");
console.log("New Age:", age);